// ── §6  PEX Field Constraint Evaluation (DIF PE v2 §5) ───────────────────────

/// Build a `credential_data` JSON string with a custom presentation definition
/// and submission for PEX field constraint evaluation tests.
fn credential_data_with_custom_pd(
    vp_token: &str,
    nonce: &str,
    pd_json: &str,
    ps_json: &str,
) -> String {
    serde_json::json!({
        "vp_token": vp_token.trim(),
        "nonce": nonce,
        "presentation_definition": serde_json::from_str::<serde_json::Value>(pd_json).unwrap(),
        "presentation_submission": serde_json::from_str::<serde_json::Value>(ps_json).unwrap(),
    })
    .to_string()
}

/// The standard descriptor map used by PEX §6 tests.
/// Navigates from the JWT root through the VP to the first verifiable credential.
const PEX_TEST_PS: &str = r#"{
    "id": "ps-pex-test",
    "definition_id": "pd-pex-test",
    "descriptor_map": [{
        "id": "credential",
        "format": "jwt_vp",
        "path": "$",
        "path_nested": {
            "id": "credential",
            "format": "jwt_vc",
            "path": "$.vp.verifiableCredential[0]"
        }
    }]
}"#;

/// DIF PE v2 §5.1: When the VP's credential satisfies the field constraint
/// filter (type array contains "UniversityDegreeCredential"), the result MUST
/// be `Valid`.
#[test]
fn pex_field_constraint_satisfied_is_valid() {
    let pd = r#"{
        "id": "pd-pex-test",
        "input_descriptors": [{
            "id": "credential",
            "constraints": {
                "fields": [{
                    "path": ["$.type"],
                    "filter": {
                        "type": "array",
                        "contains": { "const": "UniversityDegreeCredential" }
                    }
                }]
            }
        }]
    }"#;

    let data = credential_data_with_custom_pd(STATIC_VP_TOKEN, NONCE, pd, PEX_TEST_PS);
    let result = verify_oid4vp_offline(&data, VERIFIER_ID, VERIFIER_ID)
        .expect("PEX field constraint test should not error");

    assert_eq!(
        result.status,
        VerificationStatus::Valid,
        "Satisfied field constraint MUST produce Valid; warnings: {:?}",
        result.warnings
    );
}

