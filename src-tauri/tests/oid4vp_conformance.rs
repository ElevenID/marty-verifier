//! OID4VP 1.0 Final Conformance Tests — Marty Verifier (Tauri app layer).
//!
//! These tests exercise the verifier's own JSON wire format: the
//! `credential_data` JSON object accepted by the `verify_credential` Tauri
//! command.  They complement the engine-level tests in
//! `marty-core/marty-oid4vci/tests/oid4vp_conformance.rs` by adding coverage
//! of the JSON parsing + `VerificationResult` construction layers that live
//! exclusively in this crate.
//!
//! Mock: no Tauri runtime, no `AppState`.  Tests call
//! `marty_verifier::commands::verification::verify_oid4vp_offline` which
//! mirrors the offline path of `verify_oid4vp_payload` without the liveness-
//! check, license-check, and storage plumbing.
//!
//! Shared fixture corpus: same files generated by
//! `marty-integration-tests/tests/integration/fixtures/conformance/generate_fixtures.py`
//! and consumed by the Python and Rust engine suites.
//!
//! Key constants (must match the generate_fixtures.py values):
//!   VERIFIER_ID = "https://verifier.example.com"
//!   NONCE       = "n-0S6_WzA2Mj"
//!   (static VP token has aud = VERIFIER_ID, nonce = NONCE, exp = 9999999999)

#![cfg(feature = "oid4vp")]

use marty_verifier::commands::verification::{verify_oid4vp_offline, VerificationStatus};

// ── Shared fixture corpus ─────────────────────────────────────────────────────

const VERIFIER_ID: &str = "https://verifier.example.com";
const NONCE: &str = "n-0S6_WzA2Mj";

const STATIC_VP_TOKEN: &str = include_str!(
    "../../../marty-integration-tests/tests/integration/fixtures/conformance/vp_token_jwt.txt"
);
const PRESENTATION_DEFINITION_JSON: &str = include_str!(
    "../../../marty-integration-tests/tests/integration/fixtures/conformance/presentation_definition.json"
);
const PRESENTATION_SUBMISSION_JSON: &str = include_str!(
    "../../../marty-integration-tests/tests/integration/fixtures/conformance/presentation_submission.json"
);

/// Build a `credential_data` JSON string in the format `verify_credential` accepts.
fn credential_data(vp_token: &str, nonce: &str) -> String {
    serde_json::json!({
        "vp_token": vp_token.trim(),
        "nonce": nonce,
    })
    .to_string()
}

fn credential_data_with_pd(vp_token: &str, nonce: &str, pd: &str, ps: &str) -> String {
    serde_json::json!({
        "vp_token": vp_token.trim(),
        "nonce": nonce,
        "presentation_definition": serde_json::from_str::<serde_json::Value>(pd).unwrap(),
        "presentation_submission": serde_json::from_str::<serde_json::Value>(ps).unwrap(),
    })
    .to_string()
}

// ── §1  JSON Parsing (verifier wire format) ──────────────────────────────────

/// `verify_credential` must return an error when `vp_token` is absent from the
/// `credential_data` JSON object.
#[test]
fn missing_vp_token_field_returns_error() {
    let data = r#"{"nonce":"n-0S6_WzA2Mj"}"#;
    let result = verify_oid4vp_offline(data, VERIFIER_ID, VERIFIER_ID);
    assert!(
        result.is_err(),
        "Expected Err when vp_token field is missing, got {:?}",
        result
    );
    let msg = result.unwrap_err().to_string();
    assert!(
        msg.contains("vp_token"),
        "Error should mention 'vp_token', got: {msg}"
    );
}

/// Non-JSON `credential_data` must be rejected immediately.
#[test]
fn credential_data_not_json_returns_error() {
    let result = verify_oid4vp_offline("not valid json", VERIFIER_ID, VERIFIER_ID);
    assert!(result.is_err(), "Expected Err for non-JSON input");
}

/// An empty JSON object (no `vp_token`) must produce a clear error.
#[test]
fn empty_json_object_returns_error() {
    let result = verify_oid4vp_offline("{}", VERIFIER_ID, VERIFIER_ID);
    assert!(result.is_err());
}

